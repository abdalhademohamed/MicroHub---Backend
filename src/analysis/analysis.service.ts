  async getPaymentMethodUsageReport(
    fromDate?: Date,
    toDate?: Date,
    branchId?: string,
  ) {
    const query = this.PaymentRepository.createQueryBuilder("p") // Alias for PaymentEntity
      .leftJoin("p.order", "o") // Join the related orders
      .leftJoin("o.reservation", "r") // Join the related reservation to access branch details
      .leftJoin("r.branch", "b") // Join the related branch details
      .where("o.status != :status", { status: OrderStatus.Canceled }) // Filter out canceled orders
      .select("p.methodEnglish", "methodEnglish") // Select payment method in English
      .addSelect("p.methodArabic", "methodArabic") // Select payment method in Arabic
      .addSelect("b.id", "branchId") // Select the branch ID
      .addSelect("b.name", "branchName") // Select the branch name
      .addSelect("COUNT(p.id)", "usageCount") // Count how many times the payment method was used
      .groupBy("b.id") // Group by branch ID
      .addGroupBy("b.name") // Group by branch name
      .addGroupBy("p.methodEnglish") // Group by payment method in English
      .addGroupBy("p.methodArabic"); // Group by payment method in Arabic

    // Apply date filters if provided
    if (fromDate) {
      query.andWhere("o.date >= :fromDate", { fromDate });
    }
    if (toDate) {
      query.andWhere("o.date <= :toDate", { toDate });
    }

    // Add branchId filtering if provided
    if (branchId) {
      query.andWhere("b.id = :branchId", { branchId });
    }

    const result = await query.getRawMany();

    // Format the result to organize by branch
    const formattedResult = result.reduce((acc, curr) => {
      const { branchId, branchName, methodEnglish, methodArabic, usageCount } =
        curr;

      // Check if the branch already exists in the accumulator
      let branch = acc.find((b) => b.branchId === branchId);
      if (!branch) {
        // If the branch doesn't exist, create a new entry
        branch = {
          branchId,
          branchName,
          paymentMethods: [], // Initialize an array for payment methods
        };
        acc.push(branch);
      }

      // Add the payment method details to the branch
      branch.paymentMethods.push({
        methodEnglish,
        methodArabic,
        usageCount: parseInt(usageCount, 10), // Convert usage count to a number
      });

      return acc;
    }, []);

    return formattedResult;
  }

