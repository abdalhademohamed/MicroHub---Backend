  // Get reservation report with optional date range
  async getReservationReport(
    fromDate?: Date,
    toDate?: Date,
    page?: number,
    limit?: number,
  ): Promise<any> {
    const query = this.ReservationRepository.createQueryBuilder(
      "reservation",
    ).leftJoinAndSelect("reservation.order", "order"); // Ensure the order is joined

    // Filter for createdAt dates
    if (fromDate) {
      query.andWhere("reservation.createdAt >= :fromDate", { fromDate });
    }

    if (toDate) {
      query.andWhere("reservation.createdAt <= :toDate", { toDate });
    }

    // Filter out canceled reservations
    query.andWhere("order.status != :canceledStatus", {
      canceledStatus: OrderStatus.Canceled,
    });

    // Total number of reservations
    const totalReservations = await query.getCount();

    // Total revenue from non-canceled reservations
    const totalRevenue = await query
      .select("SUM(reservation.totalPrice)", "totalRevenue")
      .getRawOne();

    // New customers
    const newCustomers = await query
      .select("COUNT(DISTINCT reservation.customerId)", "newCustomers") // Renamed to newCustomers
      .getRawOne();

    // Reservations by Branch with details
    const reservationsByBranch = await query
      .select(
        "branch.id as branchId, branch.name as branchName, branch.location as branchLocation, COUNT(reservation.id) as count",
      )
      .leftJoin("reservation.branch", "branch")
      .groupBy("branch.id, branch.name, branch.location")
      .getRawMany();

    // Reservations by Service with details
    const reservationsByService = await query
      .select(
        "service.id as serviceId, service.english_Name as serviceName, COUNT(reservation.id) as count",
      )
      .leftJoin("reservation.services", "service")
      .groupBy("service.id, service.english_Name") // Correcting to use the actual column name
      .getRawMany();

    // Apply pagination only if page and limit are provided
    let reservations = [];
    let totalCount = totalReservations;

    if (page && limit) {
      [reservations, totalCount] = await query
        .skip((page - 1) * limit) // Calculate offset
        .take(limit) // Set limit
        .getManyAndCount(); // Get results and count
    } else {
      // If no pagination parameters are provided, fetch all records
      reservations = await query.getMany();
    }

    return {
      totalReservations: totalCount,
      totalRevenue: totalRevenue?.totalRevenue || 0,
      newCustomers: newCustomers?.newCustomers || 0, // Updated here as well
      reservationsByBranch,
      reservationsByService,
      reservations, // Include paginated reservations
    };
  }

