  async getTotalRefunds(
    dto: GetTotalRefundsDto,
  ): Promise<
    {
      branchId: string;
      branchName: string;
      location: string;
      totalRefund: number;
    }[]
  > {
    const { branchId, fromDate, toDate } = dto;

    // Log the incoming DTO for debugging
    console.log("DTO:", dto);

    // Prepare the base query
    const query = this.OrderRepository.createQueryBuilder("order")
      .leftJoinAndSelect("order.reservation", "reservation") // Join with reservation
      .leftJoinAndSelect("reservation.branch", "branch"); // Join with branch

    // Apply date filtering if provided
    if (fromDate) {
      query.andWhere("order.createdAt >= :fromDate", {
        fromDate: new Date(fromDate),
      });
    }
    if (toDate) {
      query.andWhere("order.createdAt <= :toDate", {
        toDate: new Date(toDate),
      });
    }

    // If a branchId is provided, filter by branch id via reservation
    if (branchId) {
      query.andWhere("reservation.branch.id = :branchId", { branchId });
    }

    // Log the query before executing
    console.log("Query:", query.getSql(), query.getParameters());

    // Fetch all orders with the applied filters
    const orders = await query.getMany();

    // Log the fetched orders
    console.log("Fetched Orders:", orders);

    // Initialize refund amounts
    const branchRefunds: {
      [key: string]: { total: number; branchName: string; location: string };
    } = {};

    // Process orders to calculate refund amounts
    orders.forEach((order) => {
      if (!order.reservation || !order.reservation.branch) {
        return; // Skip orders without a reservation or branch
      }

      let refundAmount = 0; // Default refund amount
      const revenue = parseFloat(
        order.reservation.totalPrice?.toString() || "0",
      ); // Ensure it's a number

      // Check if the order is canceled
      if (order.status === OrderStatus.Canceled) {
        // Determine refund amount based on payment status
        if (order.paymentStatus === PaymentStatus.Paid) {
          refundAmount = revenue; // Full refund if fully paid
        } else if (order.paymentStatus === PaymentStatus.PartiallyPaid) {
          refundAmount = parseFloat(
            order.reservation.deposit?.toString() || "0",
          ); // Refund deposit if partially paid
        }
      }

      // Aggregate refund amount by branch using reservation's branchId
      const orderBranchId = order.reservation.branch.id; // Get branchId from reservation
      const branchName = order.reservation.branch.name; // Get branch name
      const location = order.reservation.branch.location; // Get branch location

      if (orderBranchId) {
        if (!branchRefunds[orderBranchId]) {
          branchRefunds[orderBranchId] = { total: 0, branchName, location }; // Initialize if not already set
        }
        branchRefunds[orderBranchId].total += refundAmount; // Add to total refund for that branch
      }
    });

    // Convert the refund amounts to an array of objects
    const result = Object.entries(branchRefunds).map(
      ([branchId, { total, branchName, location }]) => ({
        branchId,
        branchName,
        location,
        totalRefund: parseFloat(total.toFixed(2)), // Format to 2 decimal places
      }),
    );

    // Log the result before returning
    console.log("Total Refunds Result:", result);

    return result;
  }
