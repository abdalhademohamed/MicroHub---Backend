  async generateOrderReport(
    dto: GenerateOrderReportDto,
    userId: string,
  ): Promise<any> {
    const { fromDate, toDate, page = 1, limit = 10 } = dto;

    // Get the requesting user's details
    const requestingUser = await this.EmployeeRepository.findOne({
      where: { id: userId },
      relations: ["branch", "position"],
    });

    // Prepare the query options
    const queryBuilder = this.OrderRepository.createQueryBuilder("order")
      .leftJoinAndSelect("order.reservation", "reservation")
      .skip((page - 1) * limit)
      .take(limit);

    // If user is a receptionist, filter by their branch ID
    if (
      requestingUser?.position?.postion === Postion.RECEPTIONIST &&
      requestingUser.branch
    ) {
      queryBuilder.andWhere("reservation.branchId = :branchId", {
        branchId: requestingUser.branch.id,
      });
    }

    // Apply date filtering if provided
    if (fromDate) {
      queryBuilder.andWhere("reservation.createdAt >= :fromDate", {
        fromDate: new Date(fromDate),
      });
    }

    if (toDate) {
      queryBuilder.andWhere("reservation.createdAt <= :toDate", {
        toDate: new Date(toDate),
      });
    }
    const branch = dto.branchId?.split(",") || [];
    if (branch.length > 0) {
      queryBuilder.andWhere("reservation.branchId IN (:...branch)", { branch });
    }

    // Fetch all orders with the applied filters and pagination
    const orders = await queryBuilder.getMany();

    // Initialize counts and total revenue
    const orderStatusCounts = {
      [OrderStatus.Canceled]: 0,
      [OrderStatus.Completed]: 0,
      [OrderStatus.Pending]: 0,
      [OrderStatus.InQueue]: 0,
      [OrderStatus.Working]: 0,
      [OrderStatus.Abscent]: 0,

      // Add other statuses as needed
    };

    let totalRevenue = 0;

    // Process orders to calculate refunds, revenue, and status counts
    const orderReports = orders.map((order) => {
      let refundAmount = 0; // Default refund amount
      let revenue = parseFloat(
        order.reservation?.totalPrice?.toString() || "0",
      ); // Ensure it's a number

      // Check if the order is canceled
      if (order.status === OrderStatus.Canceled) {
        // Determine refund amount based on payment status
        if (order.paymentStatus === PaymentStatus.Paid) {
          refundAmount = revenue; // Full refund if fully paid
        } else if (order.paymentStatus === PaymentStatus.PartiallyPaid) {
          refundAmount = parseFloat(
            order.reservation?.deposit?.toString() || "0",
          ); // Refund deposit if partially paid
        }
        revenue = 0; // Set revenue to 0 for canceled orders
      } else {
        // If not canceled, add to revenue
        totalRevenue += revenue; // Update total revenue
      }

      // Count order status
      orderStatusCounts[order.status] =
        (orderStatusCounts[order.status] || 0) + 1;

      return {
        orderId: order.id,
        refundAmount,
        revenue,
        paymentStatus: order.paymentStatus,
        status: order.status,
      };
    });

    return {
      orderReports,
      totalRevenue: totalRevenue.toFixed(2), // Format to 2 decimal places
      orderStatusCounts,
    };
  }

  async getTotalDepositsByBranch(dto: GetTotalDepositsDto): Promise<any> {
    const { branchId, fromDate, toDate } = dto;

    // Build the base query
    const query = this.ReservationRepository.createQueryBuilder("reservation")
      .select("reservation.branchId", "branchId")
      .addSelect("branch.name", "branchName") // Include branch name
      .addSelect("branch.location", "location") // Include branch location
      .addSelect("SUM(reservation.deposit)", "totalDeposits")
      .addSelect("SUM(reservation.totalPrice)", "totalPrice") // Include total price of reservations
      .leftJoin("reservation.branch", "branch") // Join with BranchEntity
      .groupBy("reservation.branchId")
      .addGroupBy("branch.name") // Group by branch name
      .addGroupBy("branch.location"); // Group by branch location

    // Apply filters if branchId is provided
    if (branchId) {
      query.andWhere("reservation.branchId = :branchId", { branchId });
    }

    // Apply date filtering if provided
    if (fromDate) {
      query.andWhere("reservation.createdAt >= :fromDate", {
        fromDate: new Date(fromDate),
      });
    }

    if (toDate) {
      query.andWhere("reservation.createdAt <= :toDate", {
        toDate: new Date(toDate),
      });
    }

    // Execute the query
    const deposits = await query.getRawMany();

    // Map the result to a more structured format, calculating totalRemaining
    return deposits.map((deposit) => ({
      branchId: deposit.branchId,
      branchName: deposit.branchName,
      location: deposit.location,
      totalDeposits: parseFloat(deposit.totalDeposits) || 0, // Ensure it's a number
      totalRemaining:
        parseFloat(deposit.totalPrice) -
        (parseFloat(deposit.totalDeposits) || 0), // Calculate remaining amount
    }));
  }

