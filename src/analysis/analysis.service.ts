  async getServiceReport(query: GetServiceReportDto): Promise<any> {
    const { page, limit, fromDate, toDate } = query;

    const queryBuilder = this.ServiceRepository.createQueryBuilder("service")
      .leftJoin("service.reservations", "reservation")
      .leftJoin("reservation.customer", "customer")
      .select([
        "service.id AS service_id",
        "service.arabic_Name AS service_arabic_name",
        "service.english_Name AS service_english_name",
        "service.price AS service_price",
        "COALESCE(COUNT(DISTINCT reservation.id), 0) AS usagecount",
        "COALESCE(COUNT(DISTINCT customer.id), 0) AS customercount",
        "COALESCE(COUNT(DISTINCT reservation.id) * service.price, 0) AS totalrevenue",
      ]);

    // Apply date range filters if provided
    if (fromDate) {
      queryBuilder.where("reservation.date >= :fromDate", { fromDate });
    }
    if (toDate) {
      queryBuilder.andWhere("reservation.date <= :toDate", { toDate });
    }

    queryBuilder.groupBy("service.id");

    // Implement pagination
    const services = await queryBuilder
      .skip((page - 1) * limit)
      .take(limit)
      .getRawMany();

    // Calculate total revenue for all services
    const totalRevenueForAllServices = services.reduce(
      (sum, service) => sum + (parseFloat(service.totalrevenue) || 0),
      0,
    );

    // Return paginated services and total revenue
    return {
      services,
      totalRevenueForAllServices,
      page,
      limit,
    };
  }

