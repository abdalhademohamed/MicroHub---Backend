  async getOfferReport(filterDto: OfferReportDto): Promise<any> {
    const { fromDate, toDate, isActive, page, limit } = filterDto;

    const query = this.OfferRepository.createQueryBuilder("offer")
      .leftJoin("offer.services", "service")
      .select([
        "offer.id",
        "offer.offerName",
        "offer.startDateTime",
        "offer.endDateTime",
        "offer.discountPercentage",
        "offer.usageCount",
        "SUM(service.price * offer.usageCount) as totalRevenue", // Calculate total revenue
      ])
      .groupBy("offer.id");

    if (fromDate) {
      query.andWhere("offer.startDateTime >= :fromDate", { fromDate });
    }

    if (toDate) {
      query.andWhere("offer.endDateTime <= :toDate", { toDate });
    }

    if (isActive !== undefined) {
      query.andWhere("offer.isActive = :isActive", { isActive });
    }

    const result = await query
      .orderBy("offer.offerName", "ASC")
      .skip((page - 1) * limit)
      .take(limit)
      .getRawMany();

    return result;
  }

